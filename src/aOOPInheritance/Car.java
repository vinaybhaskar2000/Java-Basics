package aOOPInheritance;

public class Car extends Vechicle
{
	
	public Car()
	{
		System.out.println(" This is parent Class Constructor - Car");
	}
	
	public void start()
	{
		System.out.println("Car...start");
	}
	
	public void stop()
	{
		System.out.println("Car...stop");
	}
	
	public void refuel()
	{
		System.out.println("Car...refuel");
	}
	

	
}
