package feb26;

public class LoopsConcepts {

	public static void main(String[] args) throws InterruptedException 
	{
		int i = 10;
	
		while(i >= 0)
		{
		//	System.out.println(i);
			i--;
		}
		
		int k = 10;
		
		do {
			 System.out.println(k);
			 k--;
		   } 
		while (k>=1);
		
		
		
		
		int z = 1000;
		System.out.println(z);
		z = z+2;
		System.out.println(z);
		
		
	}

		
		

}
