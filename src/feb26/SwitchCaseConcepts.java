package feb26;

public class SwitchCaseConcepts {

	public static void main(String[] args) 
	{
		int i = 13;
		
		switch (i) 
		{
		case 10: System.out.println("the value of  i  = 10");
		break;
		case 11: System.out.println("the value of  i  = 11");
		break;
		case 12: System.out.println("the value of  i  = 12");
		break;
		case 13: System.out.println("the value of  i  = 13");
		break;
		default: System.out.println("The value of i is not defined");
		break;
		}
		
		

	}

}
